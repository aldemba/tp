<?php
function somme(int $nbr1, int $nbr2):int
{
    return($nbr1+$nbr2);
}
function difference(int $nbr1, int $nbr2):int
{
    return($nbr1-$nbr2);
}
function produit(int $nbr1, int $nbr2):int
{
    return($nbr1*$nbr2);
}
function division(int $nbr1, int $nbr2):float
{
    return($nbr1/$nbr2);
}
function modulo(int $nbr1, int $nbr2):int
{
    return($nbr1%$nbr2);
}
function expo(int $nbr1, int $nbr2):int
{
    return($nbr1**$nbr2);
}

?>